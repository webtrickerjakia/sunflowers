          <p>The space is built to provide tranquil, private treatment rooms for each patient, while being able to serve hundreds of patients at a time in various modalities, some alone, some with therapists and others by their side.</p>
          <p>The spiral, continuous hall enables those in need of wheelchairs and those who are Sabbath-observant to travel the building without needing to use the elevators (we also include Sabbath-mode elevators for those who wish to use them), and also enables beds and automated carts to be moved throughout the building without being delayed waiting for an elevator. Most-importantly, it enables the entire space to feel connected and organic, rather than industrial and siloed.</p>
        </div>
        <div class="text-left">
          <p>Beyond that, we wanted this to be a place that welcomes and shares learnings with practitioners and researchers from around the world, so there are integrated classrooms, lecture halls, theaters, event spaces, and trade show spaces. This means experts can learn, teach, and practice in the same place.</p>
        </div>
        <div class="text-left">
          <p>We also wanted the campus to be a place for the community, both for education and advocacy, but also because so much of mental health benefits from community involvement, from the family and beyond. For this, we include event spaces, trade show space, and a small amount of on-campus hotel space for conferences, family celebrations, and “Shabbatons”. </p>
        </div>
        <div class="text-left">
          <p>We include both a Jewish house of prayer and study as well as non-denominational prayer chapels and study rooms for other faiths, to help maintain a sense of community, connection with God and Torah, and to advance our goals of advocacy and education throughout those to whom patients often turn.</p>
        </div>
        <div class="text-left">
          <p>It is very important that the design help those entering know they are loved and that a tremendous amount of care goes into this place to help them feel loved and well. To me, that is the most-important goal of architecture – to help people feel loved and cared-for, to remind them of their Godliness. The entrance of the building is a glass ‘waterfall’ which cascades from the top, bringing more natural light into the central spaces, while the vast, light-filled entry foyer fills incoming patients and loved ones with a sense of awe and impresses upon them that this is a special place created to help them.</p>
          <p>Around the central building, we have large and small spaces in the shape of sunflower leaves. These spaces include greenhouses, event spaces, an atrium, learning spaces, and technical facilities. </p>
          <p>All parking and entrance by vehicles, such as ambulances, is underground, from the outer perimeter of the campus, so no patient is distracted by flashing lights or noise, and so the campus is pedestrian friendly and tranquil.
